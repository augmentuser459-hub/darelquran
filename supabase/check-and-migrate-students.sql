-- ============================================================================
-- الخطوة 1: التحقق من وجود طلاب من فلسطين أو لبنان
-- ============================================================================

SELECT 
    s.id,
    s.name as "اسم الطالب",
    s.email as "البريد الإلكتروني",
    c.name_ar as "الدولة",
    c.name as "Country"
FROM students s
JOIN countries c ON s.country_id = c.id
WHERE c.name IN ('فلسطين', 'لبنان')
   OR c.name_ar IN ('فلسطين', 'لبنان')
   OR c.name_en IN ('Palestine', 'Lebanon');

