-- ============================================================================
-- حذف فلسطين ولبنان من قائمة الدول
-- Remove Palestine and Lebanon from countries list
-- ============================================================================
-- تاريخ: 2024
-- الغرض: إزالة فلسطين ولبنان من اختيارات الدول المتاحة للطلاب
-- ============================================================================

-- حذف الدول (سيتم حذف خطط التسعير المرتبطة تلقائياً بسبب ON DELETE CASCADE)
DELETE FROM countries 
WHERE name IN ('فلسطين', 'لبنان')
   OR name_ar IN ('فلسطين', 'لبنان')
   OR name_en IN ('Palestine', 'Lebanon');

-- عرض الدول المتبقية للتأكيد
SELECT name_ar, name_en, display_order 
FROM countries 
ORDER BY display_order;
